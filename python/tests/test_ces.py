"""
Comparison tests: CES Python vs R ces() / auto.ces() reference outputs.

Reference outputs were generated by:
  Rscript "tests/R scripts/ces_reference.R"

Each test case checks that Python's CES produces identical results to R:
  - Complex smoothing parameters (a, b)
  - Fitted values, residuals, forecasts
  - logLik, IC values (AIC, AICc, BIC, BICc)
  - Scale, persistence, states
"""

import json
from pathlib import Path

import numpy as np
import pandas as pd
import pytest

from smooth import CES, AutoCES
from smooth.adam_general import _adamCore
from smooth.adam_general.core.ces.cost_function import ces_cf
from smooth.adam_general.core.ces.creator import ces_creator

# ---------------------------------------------------------------------------
# Load reference data
# ---------------------------------------------------------------------------

_DATA_DIR = Path(__file__).parent / "data"
_REF_FILE = _DATA_DIR / "ces_reference.json"

with _REF_FILE.open() as _f:
    _REF = json.load(_f)


def _load_case(case_name):
    """Return (y, ref_dict) for the given case."""
    ref = _REF[case_name]
    df = pd.read_csv(_DATA_DIR / ref["data_file"])
    y = df["y"].values
    return y, ref


# ---------------------------------------------------------------------------
# Tolerances
# ---------------------------------------------------------------------------
# Tight for deterministic outputs, slightly looser for optimizer-dependent values.
# CES has a flat loss surface in some cases, so the optimizer may converge to
# a slightly different (but equally good) point — hence generous tolerances
# on B, persistence, fitted/residuals/forecasts/states.
ATOL_TIGHT = 1e-6
RTOL_TIGHT = 1e-6
ATOL_FITTED = 3.0
RTOL_FITTED = 1e-2
ATOL_PARAM = 1e-1
RTOL_PARAM = 1e-1
ATOL_IC = 1.5


# ---------------------------------------------------------------------------
# Helper to fit a CES model from reference params
# ---------------------------------------------------------------------------
def _fit_ces(y, ref):
    """Fit a CES model using params from reference."""
    params = ref["python_params"]
    m = CES(
        seasonality=params["seasonality"],
        h=params["h"],
        holdout=params["holdout"],
        lags=params.get("lags"),
        initial=params.get("initial", "backcasting"),
    )
    m.fit(y)
    return m


def _ces_cf_from_reference(case_name):
    y, ref = _load_case(case_name)
    params = ref["python_params"]
    h = params["h"]
    obs_in_sample = len(y) - h if params["holdout"] and h > 0 else len(y)
    y_in_sample = y[:obs_in_sample]
    seasonality = params["seasonality"]
    lags = [int(lag) for lag in params.get("lags", [1])]
    y_frequency = max(lags)
    lags_model_seasonal = [lag for lag in lags if lag > 1] if y_frequency > 1 else lags
    if not lags_model_seasonal:
        lags_model_seasonal = lags
    n_seasonal = len(lags_model_seasonal)

    if seasonality == "none":
        components_number = 2
        lags_model_all = [1, 1]
    elif seasonality == "simple":
        components_number = 2 * n_seasonal
        lags_model_all = [lag for lag in lags_model_seasonal for _ in range(2)]
    elif seasonality == "partial":
        components_number = 2 + n_seasonal
        lags_model_all = [1, 1, *lags_model_seasonal]
    else:
        components_number = 2 + 2 * n_seasonal
        lags_model_all = [1, 1, *[lag for lag in lags_model_seasonal for _ in range(2)]]

    lags_model_max = max(lags_model_all)
    created = ces_creator(
        seasonality=seasonality,
        n_seasonal=n_seasonal,
        lags_model_seasonal=lags_model_seasonal,
        lags_model_all=lags_model_all,
        lags_model_max=lags_model_max,
        components_number=components_number,
        xreg_number=0,
        obs_in_sample=obs_in_sample,
        obs_states=obs_in_sample + lags_model_max,
        obs_all=len(y),
        y_in_sample=y_in_sample,
        y_frequency=y_frequency,
        lags=lags,
    )

    adam_cpp = _adamCore.adamCore(
        lags=np.array(lags_model_all, dtype=np.uint64),
        E="A",
        T="N",
        S="N",
        nNonSeasonal=0,
        nSeasonal=0,
        nETS=0,
        nArima=components_number,
        nXreg=0,
        nComponents=len(lags_model_all),
        constant=False,
        adamETS=False,
    )

    b_estimate = seasonality in ("partial", "full")
    return ces_cf(
        B=np.array(ref["B"], dtype=np.float64),
        mat_vt=created["mat_vt"],
        mat_wt=created["mat_wt"],
        mat_f=created["mat_f"],
        vec_g=created["vec_g"],
        a={"value": None, "estimate": True},
        b={
            "value": None,
            "estimate": b_estimate,
            "number": (
                1 if seasonality == "partial" else 2 if seasonality == "full" else 0
            ),
        },
        seasonality=seasonality,
        n_seasonal=n_seasonal,
        lags_model_seasonal=lags_model_seasonal,
        lags_model_max=lags_model_max,
        initial_type=params.get("initial", "backcasting"),
        xreg_model=False,
        xreg_number=0,
        initial_xreg_estimate=False,
        components_number=components_number,
        lags_model_all=lags_model_all,
        index_lookup_table=created["index_lookup_table"],
        profiles_recent_table=created["profiles_recent_table"],
        y_in_sample=y_in_sample,
        ot=np.ones(obs_in_sample, dtype=np.float64),
        ot_logical=np.ones(obs_in_sample, dtype=bool),
        obs_in_sample=obs_in_sample,
        n_iterations=(
            2
            if params.get("initial", "backcasting") in ("backcasting", "complete")
            else 1
        ),
        bounds="admissible",
        loss="likelihood",
        h=h,
        multisteps=False,
        adam_cpp=adam_cpp,
    )


# ---------------------------------------------------------------------------
# Generic test runner for a CES case
# ---------------------------------------------------------------------------
class CESCaseTests:
    """Mixin that implements all comparison tests for a single CES case."""

    case_name: str

    @pytest.fixture(autouse=True)
    def setup_model(self):
        self.y, self.ref = _load_case(self.case_name)
        self.m = _fit_ces(self.y, self.ref)

    def test_model_name(self):
        assert self.m.model_name == self.ref["model_name"]

    def test_a_real(self):
        assert np.isclose(
            self.m.a_.real, self.ref["a_real"], atol=ATOL_PARAM, rtol=RTOL_PARAM
        ), f"a.real: {self.m.a_.real} vs R {self.ref['a_real']}"

    def test_a_imag(self):
        assert np.isclose(
            self.m.a_.imag, self.ref["a_imag"], atol=ATOL_PARAM, rtol=RTOL_PARAM
        ), f"a.imag: {self.m.a_.imag} vs R {self.ref['a_imag']}"

    def test_loglik(self):
        assert np.isclose(
            self.m.loglik, self.ref["logLik"], atol=ATOL_IC, rtol=RTOL_TIGHT
        ), f"logLik: {self.m.loglik} vs R {self.ref['logLik']}"

    def test_loss_value(self):
        assert np.isclose(
            self.m.loss_value, self.ref["loss_value"], atol=ATOL_IC, rtol=RTOL_TIGHT
        ), f"loss: {self.m.loss_value} vs R {self.ref['loss_value']}"

    def test_aic(self):
        assert abs(self.m.aic - self.ref["aic"]) < ATOL_IC, (
            f"AIC: {self.m.aic} vs R {self.ref['aic']}"
        )

    def test_aicc(self):
        assert abs(self.m.aicc - self.ref["aicc"]) < ATOL_IC, (
            f"AICc: {self.m.aicc} vs R {self.ref['aicc']}"
        )

    def test_bic(self):
        assert abs(self.m.bic - self.ref["bic"]) < ATOL_IC, (
            f"BIC: {self.m.bic} vs R {self.ref['bic']}"
        )

    def test_bicc(self):
        assert abs(self.m.bicc - self.ref["bicc"]) < ATOL_IC, (
            f"BICc: {self.m.bicc} vs R {self.ref['bicc']}"
        )

    def test_scale(self):
        assert np.isclose(
            self.m.scale_, self.ref["scale"], atol=ATOL_PARAM, rtol=RTOL_PARAM
        ), f"scale: {self.m.scale_} vs R {self.ref['scale']}"

    def test_b_vector(self):
        r_B = np.array(self.ref["B"])
        p_B = np.array(self.m.coef)
        assert np.allclose(p_B, r_B, atol=ATOL_PARAM, rtol=RTOL_PARAM), (
            f"B: {p_B} vs R {r_B}"
        )

    def test_persistence(self):
        r_pers = np.array(self.ref["persistence"])
        p_pers = np.array(self.m.persistence_vector)
        assert np.allclose(p_pers, r_pers, atol=ATOL_PARAM, rtol=RTOL_PARAM), (
            f"persistence: {p_pers} vs R {r_pers}"
        )

    def test_fitted_values(self):
        r_fitted = np.array(self.ref["fitted"])
        p_fitted = np.array(self.m.fitted)
        assert np.allclose(p_fitted, r_fitted, atol=ATOL_FITTED, rtol=RTOL_FITTED), (
            f"Max fitted diff: {np.max(np.abs(p_fitted - r_fitted))}"
        )

    def test_residuals(self):
        r_resid = np.array(self.ref["residuals"])
        p_resid = np.array(self.m.residuals)
        assert np.allclose(p_resid, r_resid, atol=ATOL_FITTED, rtol=RTOL_FITTED), (
            f"Max residual diff: {np.max(np.abs(p_resid - r_resid))}"
        )

    def test_forecast(self):
        h = self.ref["python_params"]["h"]
        fc = self.m.predict(h=h)
        p_fc = fc.mean.values
        r_fc = np.array(self.ref["forecast"])
        assert np.allclose(p_fc, r_fc, atol=ATOL_FITTED, rtol=RTOL_FITTED), (
            f"Max forecast diff: {np.max(np.abs(p_fc - r_fc))}"
        )

    def test_states_shape(self):
        r_nrow = self.ref["states_nrow"]
        r_ncol = self.ref["states_ncol"]
        p_states = self.m.states
        assert p_states.shape == (r_nrow, r_ncol), (
            f"States shape: {p_states.shape} vs R ({r_nrow}, {r_ncol})"
        )

    def test_states_first_row(self):
        r_first = np.array(self.ref["states_first_row"])
        p_first = self.m.states[0, :]
        assert np.allclose(p_first, r_first, atol=ATOL_FITTED, rtol=RTOL_FITTED), (
            f"States first row: {p_first} vs R {r_first}"
        )

    def test_states_last_row(self):
        r_last = np.array(self.ref["states_last_row"])
        p_last = self.m.states[-1, :]
        assert np.allclose(p_last, r_last, atol=ATOL_FITTED, rtol=RTOL_FITTED), (
            f"States last row: {p_last} vs R {r_last}"
        )


